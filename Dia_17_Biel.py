# Desafio 17: Crie um dicionário para armazenar informações de uma pessoa (nome, idade, cidade).

# Salvando o dicionário
dict = {'Nome': 'Gabriel', 'Idade': 12, 'Cidade': 'São Paulo'}

# Imprimindo o Nome do usuário
print(f"Nome: {dict['Nome']}")

# Imprimindo a Idade do usuário
print(f"Idade: {dict['Idade']}")

# Imprimindo a Cidade a qual o usuário reside
print(f"Cidade: {dict['Cidade']}")
