# Desafio 18: Adicione uma nova chave, atualize um valor e use .keys() e .values() para imprimir as chaves e valores separadamente.

# Criando outro dict:
dict = {"Name": "Gabriel", "Age": 12, "City": "São Paulo"}

# Imprimindo as chaves, e os valores separadamente

# Imprimindo as chaves
print(f"Chaves do dicionário: {tuple(dict.keys())}")

# Imprimino os valores das chaves
print(f"Valores das chaves: {tuple(dict.values())}")
